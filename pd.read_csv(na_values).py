# pd.read_csv() 関数の na_values パラメータは、CSVファイルを読み込む際に、欠損値（Missing Values）として扱うべき値を指定するためのオプションです。欠損値は通常、データの中に空白や特定の文字列など、意味がないまたは不明な値が含まれている場合に発生します。
# na_values パラメータに指定された値は、CSVファイル内のデータと比較され、一致する場合に欠損値として扱われます。これにより、データを正確に解析するために欠損値を適切に処理できます。
# 例えば、以下のようなCSVファイルがあるとします：

# Name,Age,Salary
# Alice,25,30000
# Bob,,40000
# Charlie,30,NA
# David,28,-1
# Eve,35,9999

# このCSVファイルをPandasで読み込む際に、na_values パラメータを使用して欠損値を指定することができます。例えば、na_values を次のように設定すると：

# import pandas as pd

na_values = ['', 'NA', -1, 9999]
df = pd.read_csv('xxx.csv', na_values=na_values)
# この場合、CSVファイル内のデータが ''、'NA'、-1、9999 に一致する場合、それらは欠損値として扱われます。結果として、df データフレームには欠損値が適切にマークされ、Pandasの欠損値処理機能を使用して後続の分析や操作を行うことができます。

# na_values パラメータは、特定のデータセットにおいてどの値が欠損値として扱われるべきかをカスタマイズする際に便利です。
