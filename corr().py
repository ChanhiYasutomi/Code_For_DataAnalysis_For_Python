# corr()メソッドは、Pandas DataFrameの相関行列を計算するために使用されます。以下は、具体的な例を挙げて説明します。
import pandas as pd
import numpy as np

# ダミーデータ生成
np.random.seed(42)
data = pd.DataFrame({
    'A': np.random.randn(100),
    'B': np.random.randn(100),
    'C': np.random.randn(100)
})

# 相関行列の計算
correlation_matrix = data.corr()

print("相関行列:")
display(correlation_matrix)

# この例では、3つの列（A、B、C）を持つダミーデータを生成し、それらの列の間のピアソン相関行列を計算しています。デフォルトでは、corr()メソッドはピアソン相関係数を計算します。
# このコードの出力は、各列のペアに対する相関係数が含まれる正方行列です。結果は以下のようになるかもしれません：
# 相関行列:
#           A         B         C
# A  1.000000  0.059613  0.058104
# B  0.059613  1.000000 -0.019120
# C  0.058104 -0.019120  1.000000

# この相関行列では、A列とB列の相関係数が約0.06、A列とC列の相関係数が約0.058、B列とC列の相関係数が約-0.019などが示されています。
# このように、corr()メソッドは各変数のペアの相関係数を計算し、結果を行列で返します。


