# .interpolate() メソッドは、Pandasデータフレームの欠損値を補完するために使用される方法です。具体的な例を挙げて説明しましょう。
# 以下は、.interpolate() メソッドを使用して欠損値を補完する例です。
import pandas as pd
import numpy as np

# サンプルのデータフレームを作成
data = {'A': [1, 2, np.nan, 4, 5],
        'B': [np.nan, 22, 33, np.nan, 55],
        'C': [100, np.nan, 300, 400, np.nan]}
df = pd.DataFrame(data)

# 欠損値を補完する
df_interpolated = df.interpolate()

# 結果を表示
print("元のデータフレーム:")
display(df)
print("\n欠損値を補完したデータフレーム:")
display(df_interpolated)

# このコードは、以下のようなデータフレーム df を作成し、欠損値を補完します。
#      A     B      C
# 0  1.0  22.0  100.0
# 1  2.0  22.0    NaN
# 2  NaN  33.0  300.0
# 3  4.0   NaN  400.0
# 4  5.0  55.0    NaN

# df.interpolate() を使用することで、欠損値が線形に補完されます。つまり、欠損値は前後の非欠損値の値を使用して、その間を線形に埋めることができます。

# 補完後のデータフレーム df_interpolated の結果は次の通りです：
#      A     B      C
# 0  1.0  22.0  100.0
# 1  2.0  22.0  200.0
# 2  3.0  33.0  300.0
# 3  4.0  44.0  400.0
# 4  5.0  55.0  400.0

# .interpolate() メソッドにはさまざまな補完方法とオプションがあり、線形補完以外にも多項式補完やスプライン補完などが利用できます。欠損値を適切に補完するために、データの性質に応じて適切な補完方法を選択できます。
