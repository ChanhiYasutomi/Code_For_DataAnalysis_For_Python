# zip() は、Pythonのビルトイン関数で、複数のイテラブル（リスト、タプル、文字列など）から要素を1つずつ取り出し、それらを組み合わせて新しいイテラブルを生成するのに使用されます。具体的な例を示します。

# サンプルのリストを作成
names = ['Alice', 'Bob', 'Charlie']
scores = [95, 88, 92]

# zip() を使って2つのリストを組み合わせる
zipped_data = zip(names, scores)

# 新しいイテラブルをリストに変換して表示
zipped_list = list(zipped_data)
print(zipped_list)

# このコードでは、zip() を使用して names リストと scores リストを組み合わせ、新しいイテラブル zipped_data を生成しています。その後、この新しいイテラブルをリストに変換して表示しています。
# 出力結果は次のようになります：

# [('Alice', 95), ('Bob', 88), ('Charlie', 92)]

# zip() を使用することで、複数のリストから対応する要素を組み合わせてタプルのペアを作成できます。このような操作は、データをペアとして結びつけたり、複数のリストを一度に処理したりする際に便利です。また、リストの要素数が異なる場合、zip() は最短のリストの長さに合わせて組み合わせます。
