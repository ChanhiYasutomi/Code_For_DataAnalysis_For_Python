# .dt.hour は、Pandasの日時列から各日時の時間部分（時刻）を取得するための属性です。具体的な例を以下に示します：

import pandas as pd

# サンプルのデータフレームを作成
data = {
    'timestamp': ['2023-09-15 08:30:00', '2023-09-15 14:45:00', '2023-09-15 20:15:00'],
}

df = pd.DataFrame(data)

# 日時文字列を日時列に変換
df['timestamp'] = pd.to_datetime(df['timestamp'])

# 各日時から時刻を取得
df['hour'] = df['timestamp'].dt.hour

# このコードでは、以下のことが行われています：
# サンプルのデータフレーム df を作成し、timestamp 列に日時文字列を格納します。
# pd.to_datetime を使用して timestamp 列を日時列に変換します。
# .dt.hour を使用して、各日時から時刻（時間部分）を取得し、新しい列 hour として追加します。
# 結果として、データフレームには各日時の時刻情報（時）が含まれ、hour 列に表示されます。
# 出力結果は以下のようになります：

#             timestamp  hour
# 0 2023-09-15 08:30:00     8
# 1 2023-09-15 14:45:00    14
# 2 2023-09-15 20:15:00    20

# このように、.dt.hour を使用することで、日時列から時刻情報を取得し、データの操作や分析に活用できます。
