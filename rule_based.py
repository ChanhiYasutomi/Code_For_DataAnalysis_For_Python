# ルールベースのプログラムは、特定のルールや条件に基づいて処理を行うプログラムです。ルールベースのプログラムは、特定の条件が真である場合に特定のアクションを実行するために使用されます。以下に、Pythonでルールベースのプログラムを作成する具体的な例を示します。
# 例として、あるカフェの顧客の年齢に応じて、ドリンクの割引を提供するプログラムを作成します。以下がPythonコードの例です：

# 顧客の年齢を取得
customer_age = 15

# ルールベースの割引プログラム
def calculate_discount(age):
    if age < 18:
        return 0.2  # 未成年の場合、20%割引
    elif age >= 60:
        return 0.25  # 60歳以上の場合、25%割引
    else:
        return 0.0  # それ以外の場合、割引なし

# 割引率を計算
discount_rate = calculate_discount(customer_age)

# 割引を適用して価格を計算
drink_price = 200  # ドリンクの基本価格
discounted_price = drink_price - (drink_price * discount_rate)

# 結果を出力
print(f"顧客の年齢: {customer_age}歳")
print(f"割引率: {discount_rate * 100}%")
print(f"割引後の価格: {discounted_price:.2f}円")

# このコードでは、顧客の年齢に応じて割引率を計算し、割引後の価格を算出します。具体的なルールは次のようになっています：
# 未成年（18歳未満）の場合は20%割引。
# 60歳以上の場合は25%割引。
# それ以外の場合は割引なし。
# このように、ルールベースのプログラムでは条件に応じて異なるアクションを実行するためのルールが定義され、それに従ってプログラムが振る舞います。
# この例では年齢に基づいたルールが示されていますが、実際のアプリケーションではさまざまな条件やルールを適用できます。
