# np.arange(n) は、NumPyライブラリを使用して、0から n-1 までの整数を生成するための関数です。以下は具体的な例です。
import numpy as np

# 0から4までの整数を生成
n = 5
arr = np.arange(n)

# 結果を表示
print(arr)

# このコードでは、np.arange(n) を使用して n が5の場合、0から4までの整数が含まれたNumPy配列が生成されます。生成された配列 arr を表示すると、次のようになります：
# [0 1 2 3 4]
# このように、np.arange(n) は連続した整数のシーケンスを生成するのに便利な関数です。シーケンスの開始値、終了値、ステップサイズなどをカスタマイズすることも可能です。たとえば、np.arange(2, 10, 2) は2から9までの偶数を生成します。
