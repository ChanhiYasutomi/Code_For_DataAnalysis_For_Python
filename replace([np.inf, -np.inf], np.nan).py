# replace([np.inf, -np.inf], np.nan) は、Pandasを使用して、データフレーム内の無限大（np.infまたは-np.inf）を欠損値（np.nan）に置き換える方法を示しています。
# これにより、無限大の値が含まれるデータを扱いやすくすることができます。以下に具体的な例を示します。
# まず、サンプルデータを含むデータフレームを作成します：
import pandas as pd
import numpy as np

data = {'A': [1, 2, 3, np.inf, 5],
        'B': [6, 7, -np.inf, 9, 10]}

df = pd.DataFrame(data)

# このデータフレーム df には、'A' 列と 'B' 列に無限大の値が含まれています。次に、replace([np.inf, -np.inf], np.nan) を使用して無限大を欠損値に置き換えます：

df = df.replace([np.inf, -np.inf], np.nan)

# このコードは、np.inf と -np.inf を見つけて、それらを np.nan に置き換えます。
# 結果として、データフレーム df は次のようになります：
#      A    B
# 0  1.0  6.0
# 1  2.0  7.0
# 2  3.0  NaN
# 3  NaN  9.0
# 4  5.0 10.0
# 無限大が欠損値に置き換えられ、データフレーム内で適切に処理できるようになりました。このような操作は、データセット内の異常値を処理したり、統計情報を計算したりする際に役立ちます。
