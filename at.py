# .at は、Pandasデータフレームやシリーズ内の特定のセル（要素）の値を高速に取得または設定するためのメソッドです。.at メソッドは、行と列のラベル（インデックスやカラム名）を指定してデータにアクセスします。以下は具体的な例です。

import pandas as pd

# サンプルのデータフレームを作成
data = {
    'Name': ['Alice', 'Bob', 'Charlie'],
    'Age': [25, 30, 22],
    'City': ['New York', 'Los Angeles', 'Chicago']
}

df = pd.DataFrame(data)

# 特定のセルの値を取得
name_of_bob = df.at[1, 'Name']  # 行インデックス 1 と 'Name' カラムのセルの値を取得

# 特定のセルの値を設定
df.at[2, 'Age'] = 23  # 行インデックス 2 と 'Age' カラムのセルの値を 23 に設定

# このコードでは、以下のことが行われています：
# サンプルのデータフレーム df を作成します。このデータフレームには3つの列 'Name'、'Age'、'City' が含まれています。
# .at メソッドを使用して、特定のセルの値を取得します。例えば、df.at[1, 'Name'] は行インデックス 1（Bobの行）と 'Name' カラムのセルの値を取得します。
# .at メソッドを使用して、特定のセルの値を設定します。例えば、df.at[2, 'Age'] は行インデックス 2（Charlieの行）と 'Age' カラムのセルの値を 23 に設定します。
# 結果を表示して、取得および設定された値を確認します。

# 出力結果は次のようになります：

#       Name  Age         City
# 0    Alice   25     New York
# 1      Bob   30  Los Angeles
# 2  Charlie   23      Chicago

# .at メソッドは、特定のセルの値を高速に取得または設定するための便利な方法です。特にデータフレームが大きい場合、.at メソッドは効率的な選択肢となります。
