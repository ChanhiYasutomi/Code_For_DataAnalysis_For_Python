# Pandasデータフレーム内の条件に基づいてフラグ列を作成します。具体的な例として、年齢が30歳以上である場合にフラグを設定することを考えてみましょう。
import pandas as pd

# 仮のデータフレームを作成
data = {'名前': ['Alice', 'Bob', 'Charlie', 'David', 'Eve'],
        '年齢': [25, 30, 22, 35, 28],
        '都市': ['New York', 'Los Angeles', 'Chicago', 'Houston', 'New York']}

df = pd.DataFrame(data)

# 条件に基づいてフラグ列を作成
df['フラグ'] = (df['年齢'] >= 30) * 1

# フラグ列が条件を満たす場合に1に設定され、そうでない場合に0に設定されました。具体的には、年齢が30歳以上の行のみがフラグ1を持っています。
