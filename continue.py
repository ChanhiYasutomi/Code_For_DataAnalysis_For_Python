# continue ステートメントは、Pythonの for ループ内で使用され、ループの現在のイテレーションをスキップして、次のイテレーションに進むために使用されます。具体的な例を見てみましょう。
# 以下は、1から10までの数値を順番に表示する for ループの例です。ただし、if 文を使用して偶数の場合は表示せず、奇数の場合のみ表示します。
for i in range(1, 11):
    if i % 2 == 0:  # 偶数の場合
        continue  # このイテレーションをスキップして次のイテレーションに進む
    print(i)  # 奇数の場合にのみ表示されます
  
# このコードでは、for ループが1から10までの数値をイテレーションします。if i % 2 == 0 の条件が成立する場合（つまり i が偶数の場合）、continue ステートメントが実行され、そのイテレーションがスキップされます。
# 奇数の場合は print(i) が実行され、その数値が表示されます。
# 実行結果は次のようになります：

1
3
5
7
9

# このように、continue ステートメントは特定の条件を満たす場合に、ループ内の一部の処理をスキップするのに役立ちます。
