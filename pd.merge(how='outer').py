# pd.merge() メソッドは、Pandasライブラリを使用して、異なるデータフレームを結合またはマージするために使われます。
# how パラメータを指定することにより、結合の方法を制御できます。how='outer' を指定すると、外部結合（outer join）が行われます。
# 外部結合は、両方のデータフレームのすべての行を保持し、一致しない部分を欠損値で埋める方法です。
# 以下に具体的な例を示します。

# まず、2つのサンプルデータフレームを作成します：
import pandas as pd

# データフレーム1
data1 = {'ID': [1, 2, 3],
         'Name': ['Alice', 'Bob', 'Charlie']}
df1 = pd.DataFrame(data1)

# データフレーム2
data2 = {'ID': [2, 3, 4],
         'Age': [30, 35, 40]}
df2 = pd.DataFrame(data2)

# これで、df1 と df2 という2つのデータフレームが用意されました。これらを ID 列をキーとして外部結合します：
merged_df = pd.merge(df1, df2, on='ID', how='outer')

# このコードでは、pd.merge() メソッドを使用して df1 と df2 を ID 列をキーとして外部結合しています。結合の方法を how='outer' と指定することで、すべての行を保持し、一致しない部分を欠損値で埋めます。
# 結果として、merged_df という新しいデータフレームが生成され、以下のようになります：

#    ID     Name   Age
# 0   1    Alice   NaN
# 1   2      Bob  30.0
# 2   3  Charlie  35.0
# 3   4      NaN  40.0

# この結果を見ると、ID 列をキーとして外部結合され、両方のデータフレームのすべての行が含まれています。一致しない部分は NaN で埋められています。外部結合は、2つのデータソースを結合して、データの完全性を保つ場合に便利です。
