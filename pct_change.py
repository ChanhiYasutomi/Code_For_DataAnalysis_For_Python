# .pct_change() メソッドは、Pandasデータフレームやシリーズの要素間のパーセンテージ変化を計算するために使用されます。具体的には、前の要素とのパーセンテージ変化を計算します。このメソッドは、データのトレンドや変化率を分析するのに役立ちます。
# 以下に、.pct_change() メソッドの具体例を示します：
import pandas as pd

# サンプルのデータを作成
data = {'価格': [100, 105, 110, 115, 120]}
df = pd.DataFrame(data)

# パーセンテージ変化を計算
percentage_change = df['価格'].pct_change()

# 結果を表示
display(percentage_change)
# このコードでは、サンプルのデータフレーム df を作成し、'価格' 列の要素間のパーセンテージ変化を計算しています。percentage_change シリーズには、各要素の前の要素とのパーセンテージ変化が含まれています。最初の要素は NaN となります。
# 出力は次のようになります：

# 0         NaN
# 1    0.050000
# 2    0.047619
# 3    0.045455
# 4    0.043478

# Name: 価格, dtype: float64

# この出力から、各要素の前の要素とのパーセンテージ変化が計算され、最初の要素は NaN（前の要素がないため）であることがわかります。その後の要素は、前の要素に対するパーセンテージ変化が示されています。たとえば、2番目の要素は前の要素に対して約5%増加しています。
