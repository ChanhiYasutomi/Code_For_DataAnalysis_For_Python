# reduce は、Pythonの組み込み関数ではなく、functools モジュールに含まれている関数です。reduce 関数は、リストやイテラブルの要素に対して指定した関数を累積的に適用して、単一の値を返すのに使用されます。以下に具体的な例を示します。
# 例として、リスト内の要素を累積的に足し合わせる関数を定義し、それを reduce を使ってリストの要素に適用して合計を計算します。
from functools import reduce

# 関数を定義
def add(x, y):
    return x + y

# リストを作成
numbers = [1, 2, 3, 4, 5]

# reduceを使用して関数を適用し、合計を計算
total = reduce(add, numbers)

# 結果を表示
print(total)

# このコードの説明：
# add 関数は、2つの引数 x と y を受け取り、それらを足し合わせて返す関数を定義します。
# numbers リストは、整数を含むリストです。
# reduce(add, numbers) は、add 関数を numbers リストの要素に累積的に適用します。reduce 関数は最終的な合計値を返します。
# total 変数には、reduce 関数によって計算された合計値が格納されます。
# 最後に print(total) を使用して、合計値を表示します。結果は 15 となります。

# このように、reduce 関数はリストやイテラブルの要素を累積的に処理するために使用され、指定した関数を使用して累積結果を生成します。
# 上記の例では、リスト内の数値を累積的に足し合わせて合計を計算しました。
